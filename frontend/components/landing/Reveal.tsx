"use client";
import { motion } from "framer-motion";

/**
 * Scroll-reveal wrapper for the (server-rendered) landing page sections.
 * Kept as a tiny client island so app/page.tsx can stay a server component
 * and keep its static `metadata` export.
 */
export function Reveal({
  children,
  delay = 0,
  y = 24,
  className,
}: {
  children: React.ReactNode;
  delay?: number;
  y?: number;
  className?: string;
}) {
  return (
    <motion.div
      initial={{ opacity: 0, y }}
      whileInView={{ opacity: 1, y: 0 }}
      viewport={{ once: true, margin: "-80px" }}
      transition={{ duration: 0.6, delay, ease: [0.22, 1, 0.36, 1] }}
      className={className}
    >
      {children}
    </motion.div>
  );
}
